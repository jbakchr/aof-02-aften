"""
Eksempler på datatyper
"""

# Eksempel på tekst datatypen (str)
name = "Jonas"


# Eksempler på numeriske datatyper (int, float, complex)
age = 30
pi = 3.14159
complex_num = 2j


# Eksempler på sekvens datatyper (list, tuple, range)
friends = ["Alice", "Bob", "Charlie"]
coordinates = (10, 20)
sequence = range(5)


# Eksempel på 'mapping' datatyper (dict)
person = {"name": "John", "age": 25, "city": "New York"}


# Eksempler på sæt datatyper (set, frozenset)
unique_numbers = {1, 2, 3, 2}
frozen = frozenset([1, 2, 3])


# Eksempel på boolean datatypen (bool)
is_active = True
is_online = False
